usernameGen.adjectives = utilities.shuffleArray(usernameGen.adjectives);
usernameGen.nouns = utilities.shuffleArray(usernameGen.nouns);

usernameGen.generate = function () {
  var utilities = require('./utilities');
  var firstPart = usernameGen.adjectives[utilities.rand(0, usernameGen.adjectives.length)];
  var lastPart = usernameGen.nouns[utilities.rand(0, usernameGen.nouns.length)];
  
  lastPart = lastPart.charAt(0).toUpperCase() + lastPart.substr(1);

  // username is of the format adjectiveNoun
  return firstPart + lastPart;
};

module.exports = usernameGen;
